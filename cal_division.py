# 방법 1
def sumDivisor(num):
    # num / 2 의 수들만 검사하면 성능 약 2배 향상잼
    return num + sum([i for i in range(1, (num // 2) + 1) if num % i == 0])

N = int(input())
print(f"{N}의 약수의 합은 {sumDivisor(N)}")

